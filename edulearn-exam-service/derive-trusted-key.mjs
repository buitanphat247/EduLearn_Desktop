#!/usr/bin/env node
// Derive the SERVICE trusted public key from the backend's EXISTING signing
// private key (server/server-nest/.env → EXAM_POLICY_PRIVATE_KEY_PEM), so you can
// (re)install the Exam Guard service WITHOUT regenerating keys / editing .env /
// restarting the backend. Prints ONLY the public key + the ready-to-run elevated
// Setup command — the private key is never printed.
//
// Why this exists: the service pins BOTH the trusted server key AND the
// rust-core.exe hash. Rebuilding rust-core changes its hash, so the service stops
// trusting the client and every elevated kill is rejected. After any rust-core
// rebuild, re-run Setup with the SAME key — this script hands you that command.
//
// Usage:
//   node derive-trusted-key.mjs
//   # then, from an ELEVATED (Administrator) PowerShell, run the printed command.

import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, resolve } from "node:path";
import { createPrivateKey, createPublicKey } from "node:crypto";

const here = dirname(fileURLToPath(import.meta.url));
// desktop/edulearn-exam-service → repo/server/server-nest/.env
const envPath = resolve(here, "..", "..", "server", "server-nest", ".env");

let txt;
try {
  txt = readFileSync(envPath, "utf8");
} catch {
  console.error(`Cannot read backend .env at: ${envPath}`);
  process.exit(1);
}

const keyId =
  (txt.match(/^EXAM_POLICY_KEY_ID=(.*)$/m) || [])[1]?.trim() || "exam-policy-primary";
let pem = (txt.match(/^EXAM_POLICY_PRIVATE_KEY_PEM=(.*)$/m) || [])[1];
if (!pem) {
  console.error("EXAM_POLICY_PRIVATE_KEY_PEM not found in backend .env.");
  process.exit(1);
}
pem = pem.trim();
if (pem.startsWith('"') && pem.endsWith('"')) pem = pem.slice(1, -1);
const BS = String.fromCharCode(92); // backslash — avoid escaping pitfalls
pem = pem.split(BS + "n").join("\n").split("\r").join("");

let privateKey;
try {
  privateKey = createPrivateKey(pem);
} catch {
  // Fallback: strip PEM armor + whitespace and decode the PKCS#8 DER directly.
  const der = Buffer.from(pem.replace(/-----[^-]+-----/g, "").replace(/\s+/g, ""), "base64");
  privateKey = createPrivateKey({ key: der, format: "der", type: "pkcs8" });
}

const spki = createPublicKey(privateKey).export({ type: "spki", format: "der" });
const rawPublicB64 = spki.subarray(spki.length - 32).toString("base64");
const trustedKeysJson = JSON.stringify({ [keyId]: rawPublicB64 });

// Emit a ready-to-run elevated script so the operator never has to retype the
// base64 key (an O↔0 / I↔l typo silently breaks every elevated kill). Just run
// the generated file from an Administrator PowerShell.
// Single-quoted PowerShell literal — PS does NOT treat backslash as an escape,
// so the raw Windows path is correct as-is (double any embedded single quote).
const hereQuoted = resolve(here).replace(/'/g, "''");
function writeGenerated(fileName, skipBuild, note) {
  const p = resolve(here, fileName);
  writeFileSync(
    p,
    [
      "# AUTO-GENERATED by derive-trusted-key.mjs — do not edit by hand.",
      `# ${note}`,
      "# Run from an ELEVATED (Administrator) PowerShell with the desktop app CLOSED.",
      "$ErrorActionPreference = 'Stop'",
      `Set-Location -LiteralPath '${hereQuoted}'`,
      `.\\Setup-ExamGuardService.ps1 ${skipBuild ? "-SkipBuild " : ""}-TrustedServerKeysJson '${trustedKeysJson}'`,
      "sc.exe query EduLearnExamGuard",
      "",
    ].join("\r\n"),
    "utf8",
  );
  return p;
}
// Re-pin only (binaries unchanged) vs full rebuild+re-pin (after editing
// rust-core / the service source — the binary hash changes, so the service must
// re-trust the freshly built rust-core.exe).
const generatedPs1 = writeGenerated(
  "repin-service.generated.ps1",
  true,
  "Re-pin the CURRENT binaries (nothing rebuilt).",
);
const rebuildPs1 = writeGenerated(
  "rebuild-and-repin.generated.ps1",
  false,
  "REBUILD rust-core + service, then re-pin (use after changing native code).",
);

console.log(`
============================================================
 EduLearn Exam Guard — trusted key derived from backend .env
 key id: ${keyId}
============================================================

TrustedServerKeysJson (matches the running backend — do NOT edit .env):
${trustedKeysJson}

Run this from an ELEVATED (Administrator) PowerShell to (re)install the
service and re-pin the CURRENT rust-core.exe hash:
------------------------------------------------------------
cd "${resolve(here)}"
.\\Setup-ExamGuardService.ps1 -SkipBuild -TrustedServerKeysJson '${trustedKeysJson}'
------------------------------------------------------------
Then verify:  sc.exe query EduLearnExamGuard
Re-run this after EVERY rust-core rebuild (the pinned hash changes).

✅ FOOLPROOF scripts written (no key retyping). Close the desktop app first, then
   run ONE of these from an ELEVATED PowerShell:
   • After changing native code (rebuild):  & "${rebuildPs1}"
   • Re-pin only (nothing rebuilt):          & "${generatedPs1}"
`);
